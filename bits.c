/* 
 * isNegative - return 1 if x < 0, return 0 otherwise 
 *   Example: isNegative(-1) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNegative(int x) {
   int msb = x >> 31;
   int negative = msb & 1;
   return negative; 
}
/* 
 * negate - return -x 
 *   Example: negate(1) = -1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int negate(int x) {
    
  return ~x + 1;
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 3 (most significant) to 0 (least significant) 
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
   int get = x >> (n<<3);
   int byte = get & (0xFF);
  return byte;
}
/* 
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
   int getN = x >> (n<<3);
   int byteN = getN & (0xFF);
   int getM = x >> (m<<3);
   int byteM = getM & (0xFF);
   int mask1 = ~(0xFF << (n<<3));
   int mask2 = ~(0xFF << (m<<3));
   int swap = (x & mask1) & mask2;
   int putN = byteN << (m<<3);
   int putM = byteM << (n<<3);
   int final = swap | (putN | putM);

    return final;
}
/* 
 * rotateRight - Rotate x to the right by n bits
 *   Can assume that 0 <= n <= 31
 *   Examples: rotateRight(0x87654321,4) = 0x18765432
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 25
 *   Rating: 3 
 */
int rotateRight(int x, int n) {

   int shiftRight = x << 32 + (~n + 1);
   int shiftLeft = (x >> n) & ((1 << 32 + (~n + 1)) + (~1+1));
   int rotate = shiftRight | shiftLeft;
    return rotate;
}
/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
   int sum = x + y;
   int overflow = ((x ^ sum) & (y ^ sum)) >> 31;
   return !overflow;
}